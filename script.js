        const cart = [];

        function displayProducts() {
            const featuredProducts = document.getElementById('featured-products');
            const sarees = document.getElementById('sarees');
            const kurtis = document.getElementById('kurtis');
            const watches = document.getElementById('watches');
            const electronics = document.getElementById('electronics');

            const categories = {
                sarees: [],
                kurtis: [],
                watches: [],
                electronics: []
            };

            products.forEach(product => {
                const productHTML = `
                    <div class="product">
                        <img src="${product.image}" alt="${product.name}">
                        <h2>${product.name}</h2>
                        <p>$${product.price}</p>
                        <div class="rating">${'⭐'.repeat(product.rating)}</div>
                        <div class="${product.stock ? 'stock' : 'out-of-stock'}">
                            ${product.stock ? 'In Stock' : 'Out of Stock'}
                        </div>
                        ${product.stock ? `<button onclick="addToCart(${product.id})">Add to Cart</button>` : ''}
                    </div>
                `;
                categories[product.category].push(productHTML);
            });

            // Randomly select some products to feature on the home page
            const featured = products.slice(0, 4).map(product => `
                <div class="product">
                    <img src="${product.image}" alt="${product.name}">
                    <h2>${product.name}</h2>
                    <p>$${product.price}</p>
                    <div class="rating">${'⭐'.repeat(product.rating)}</div>
                    <div class="${product.stock ? 'stock' : 'out-of-stock'}">
                        ${product.stock ? 'In Stock' : 'Out of Stock'}
                    </div>
                    ${product.stock ? `<button onclick="addToCart(${product.id})">Add to Cart</button>` : ''}
                </div>
            `).join('');

            featuredProducts.innerHTML = featured;
            sarees.innerHTML = categories.sarees.join('');
            kurtis.innerHTML = categories.kurtis.join('');
            watches.innerHTML = categories.watches.join('');
            electronics.innerHTML = categories.electronics.join('');
        }

        function addToCart(productId) {
            const product = products.find(p => p.id === productId);
            cart.push(product);
            updateCartCount();
            showCart(); // Ensure cart is shown when an item is added
        }

        function updateCartCount() {
            const cartCount = document.getElementById('cart-count');
            cartCount.innerText = cart.length;
        }

        function showSection(sectionId) {
            const sections = ['home', 'shop', 'contact', 'cart', 'payment'];
            sections.forEach(id => {
                const section = document.getElementById(id);
                if (id === sectionId) {
                    section.style.display = 'block';
                    if (id === 'cart') {
                        displayCartItems();
                    }
                } else {
                    section.style.display = 'none';
                }
            });
        }

        function displayCartItems() {
            const cartItems = document.getElementById('cart-items');
            cartItems.innerHTML = cart.map(item => `
                <div class="cart-item">
                    <p>${item.name}</p>
                    <p>$${item.price}</p>
                </div>
            `).join('');
            updateCartTotal();
        }

        function updateCartTotal() {
            const cartTotal = document.getElementById('cart-total');
            const total = cart.reduce((sum, item) => sum + item.price, 0);
            cartTotal.innerText = total.toFixed(2);
        }

        function checkout() {
            const cartSection = document.getElementById('cart');
            const paymentSection = document.getElementById('payment');
            cartSection.style.display = 'none';
            paymentSection.style.display = 'block';
        }

        function submitPayment() {
            alert("Payment submitted successfully!");
            window.location.reload();
        }

